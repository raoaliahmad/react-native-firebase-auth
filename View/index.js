import 'react-native-gesture-handler'
import React, { useState, useEffect } from 'react';
import {
  SafeAreaView,
  StyleSheet,
  ScrollView,
  View,
  Text,
  StatusBar,
	Button,
} from 'react-native';

import auth from '@react-native-firebase/auth'
import BottomTabs from './Components/Tabs'
import Auth from './Pages/auth';

const App = () => {
	
	const [ initializing, setInititalizing ] = useState(true)
	const [user, setUser] = useState();

	const onAuthStateChange = (user) => {
		setUser(user)
		if (initializing) {
			setInititalizing(false)
		}
	}

	useEffect(() => {
		const subscriber = auth().onAuthStateChanged(onAuthStateChange)
		return subscriber //to unsbscribe
	}, [])
	
	if (initializing) {
		return null
	}

	if (!user) {
		return <Auth />
	}
 
	return <BottomTabs />
}

export default App






